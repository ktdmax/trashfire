    Genie.Router.route("/api/reports/custom", custom_report_handler, method=POST)
    Genie.Router.route("/api/reports/heatmap", heatmap_handler, method=GET)
    Genie.Router.route("/api/reports/compliance", compliance_report_handler, method=POST)
    Genie.Router.route("/api/reports/list", list_reports_handler, method=GET)
    Genie.Router.route("/api/reports/delete", delete_report_handler, method=DELETE)
end

