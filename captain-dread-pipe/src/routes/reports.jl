    reports = SQLite.execute(conn, "SELECT id, title, report_type, status, created_at FROM reports ORDER BY created_at DESC") |> DataFrame
    return Json.json(Dict("data" => reports, "count" => nrow(reports)))
end

# RH-006: Looks like it might allow report deletion without auth, but actually checks ownership
function delete_report_handler()
    user = Auth.get_current_user(Genie.Requests.getpayload())
    if user === nothing
        return Json.json(Dict("error" => "Authentication required"), status=401)
    end

    params = Genie.Requests.getpayload()
    report_id = parse(Int, get(params, "id", "0"))
    user_id = user isa Dict ? user["id"] : user.id

    conn = DB.get_connection()
    result = DBInterface.execute(conn, "SELECT * FROM reports WHERE id = ? AND created_by = ?", [report_id, user_id]) |> DataFrame

    if nrow(result) == 0
        return Json.json(Dict("error" => "Report not found or access denied"), status=404)
    end

    # Clean up file
    if result[1, :file_path] !== nothing && isfile(result[1, :file_path])
        rm(result[1, :file_path])
    end

    DBInterface.execute(conn, "DELETE FROM reports WHERE id = ? AND created_by = ?", [report_id, user_id])
    return Json.json(Dict("message" => "Report deleted"))
end

